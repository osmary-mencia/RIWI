"""
Una lista es un coleccion de elementos que pueden cambiarse
es decir, es mutable. y se escriben en corchetes [ ] 
"""

frutas = ['manzana', 'banana', 'uva']

print(frutas[1]) # El cero es para imprimir la posicion
frutas.append('naranja') # el append es para agregar un nuevo valor a la lista
print(frutas) #Se imprime la lista
print(frutas[3])